<br>
  <h3>Qué es PHP</h3>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
   
  <img src="images/php.jpg" style="float:left; padding: 10px;" width="300" height="200" />
  <p>A lo largo de la Web encontrarás bastante información acerca de este lenguaje, en vista de esto, es mejor resumir lo más que se pueda acerca de este importante lenguaje de programación.</p>
  <p>&nbsp;</p>
  <p>PHP es un lenguaje de programación interpretado que fue creado en el año de 1995 el cual fue hecho con el objetivo de hacer dinámicas las páginas web. Es un lenguaje de programación sencillo de manejar y fácil de aprender.<br>
    <br>
    PHP es un acrónimo  que significa PHP Hypertext Pre-processor y se encuentra diseñado solamente para el desarrollo Web, puede ser incrustado en HTML. Se ejecuta desde un servidor Web y es compatible con cualquier sistema operativo que exista en el mercado</p>
  <p>. </p>
  <p>Por ser PHP un lenguaje de programación libre, ya que fue Publicado bajo la PHP License, la Free Software Foundation, es posible verlo en casi todas las páginas web existentes en la web.</p>
  <br />
  <p>Sus lenguajes de programación similares que existen en el mercado son ASP que es tecnología de Microsoft y JSP que es tecnología de Sun Microsystems.</p>
  <p ><img style="display:block; margin:auto" src="https://upload.wikimedia.org/wikipedia/commons/thumb/2/27/PHP-logo.svg/1200px-PHP-logo.svg.png" height="300" width="600"></p>
  <br />
  <p>Permite la conexión con la gran mayoría de motores de base de datos, y destaca mas su conectividad con Mysql y PostgreSQL.</p>
  <p>&nbsp;</p>
  <p>Existen hoy en día grandes aplicaciones web desarrolladas en PHP como lo son Joomla Y Drupal que son gestores de contenido web y Moodle que es una plataforma educativa para la formación Virtual.</p>
  <p>&nbsp;</p>
  <p style="font-weight:bold">Ventajas de Usar PHP</p>
  <p>&nbsp;</p>
  <p>Completamente orientado al desarrollo de aplicaciones web dinámicas con acceso a información almacenada en una Base de Datos.</p>
  <p>El código fuente escrito en PHP es invisible al navegador y al cliente ya que es el servidor el que se encarga de ejecutar el código y enviar su resultado HTML al navegador. Esto hace que la programación en PHP sea segura y confiable.</p>
  <p>No requiere definición de tipos de variables aunque sus variables se pueden evaluar también por el tipo que estén manejando en tiempo de ejecución.</p>
  <p>&nbsp;</p>
  <p><img style="display:block; margin:auto" src="http://conceptodefinicion.de/wp-content/uploads/2014/11/php_development.gif" height="300" width="600"></p>
  <p>Tiene manejo de excepciones (desde PHP5).</p>
  <p>&nbsp;</p>
  <p>PHP no obliga a quien lo usa a seguir una determinada metodología a la hora de programar, aunque muchos otros lenguajes tampoco lo hacen, aun estando dirigido a alguna en particular, el programador puede aplicar en su trabajo cualquier técnica de programación y/o desarrollo que le permita escribir código ordenado, estructurado y manejable. </p>
  <p>&nbsp;</p>
  <p style="font-weight:bold">Desventajas</p>
  <p>&nbsp;</p>
  <p>La ofuscación de código es la única forma de ocultar los fuentes del código realizado en PHP.</p>
  <p>&nbsp;</p>
  <p>El lenguaje de programación PHP no permite control de dispositivos tales como celulares o periféricos. Solamente puede ejecutarse en navegadores web.</p>
  <p>&nbsp;</p>
  <p>Puedes encontrar varios lenguajes de programación web en el mercado, pero PHP es un lenguaje de programación web que vino para quedarse por un tiempo.</p>
